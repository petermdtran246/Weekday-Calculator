def DayName():
    day = int(input('Enter a day number: '))
    if day == 1:
        print('sun')
    elif day == 2:
        print('mon')
    elif day == 3:
        print('tue')
    elif day == 4:
        print('wed')
    elif day == 5:
        print('thu')
    elif day == 6:
        print('fri')
    elif day == 7:
        print('sat')

DayName()
